print("Ejecucuion del primer ciclo")
x = 0
while x <= 15: #Se ejecuta el primer ciclo mientras que x <= 15
    print(x)
    x += 5 #Se incrementa en 5
print("\n\n")

print("Ejecucion del segundo ciclo")
x = 0
while x >= -100: #Se ejecuta el primer ciclo mientras que x >= -100
    print(x)
    x -= 20 #Se decrementa en -20
print("\n\n")

print("Ejecucion del tercer ciclo")
x = 10
while x >= 0:
    print('El valor del bucle es: ' + str(x)) #Se imprime el resultado hasta que x sea 0
    x -= 1 #Se decrementa en -1